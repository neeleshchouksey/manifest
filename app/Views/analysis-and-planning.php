            <p class="justify process_prg">Website analysis in the web development lifecycle is, really, the very first phase of the process.
              This is where the web developers take the requirements you bring them by answering the 5 key questions: <br/>
            <ul class="bullet_process check_right check_ic span">
              <li><i class="fa fa-circle-o"></i>Why do you need a website?</li>
              <li><i class="fa fa-circle-o"></i>Who is your target audience?</li>
              <li><i class="fa fa-circle-o"></i>What will the website do for you?</li>
              <li><i class="fa fa-circle-o"></i>Where will you host the website?</li>
              <li><i class="fa fa-circle-o"></i>When do you want the project finished?</li>
            </ul>
           
            <p class="justify process_prg"> The answers to each of these questions helps the web developer build the foundation of the analysis. It is the puzzle pieces to help
              build the specifications and outline of the project. From these answers the web developer will slowly piece together the puzzle, 
              tossing out pieces that don't fit or adding in new ones when required. </p>
            <p class="justify process_prg"> Even after answering the 5 questions you can expect many more. The web developer takes the answers and refines them by asking more. 
