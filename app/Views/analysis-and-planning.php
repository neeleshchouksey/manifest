            <h3>Website Usability</h3><br />
            <p class="justify process_prg">Defining usability needs accounts for 75% of the final analysis. What good is a website 
              that nobody wants to navigate, read, purchase from, use, or suggest to their friends? Many of the questions asked during 
              the website development lifecycle analysis revolve around usability. </p>
            
            <p class="justify process_prg">From a programming standpoint coding an email contact form is a bunch of letters and syntax. 
              If the visitor saw the results of the unrefined code they would likely find the visual form unprofessional and boring. 
              If it isn't important they will find something else to do then use the form. </p>
            <br />
            <ul class="bullet_process check_ic">
              <li>25% of the analysis is the requirements of the contact form - what it needs to do.</li>
              <li>75% of the analysis is visually creating it so people want to use it.</li>
            </ul>
            
            <p class="justify process_prg">Website usability is beyond the scope of this article, but I hope you can see how it is fundamental to 
              the analysis phase. The analysis is the beautiful Ikea table you bought. It is clearly defined as the final outcome of the project.
              You know what it is, what it is meant to do, how it best fits in your current workflow and whether you want to use it from the very 
              first glance. </p>
