            <p class="justify process_prg">If you are learning about Web technology, you may wish to start with the introduction below, and follow links for greater detail.</p>
            <br />
            <p class="justify process_prg"><b>Web Design and Applications :</b> Web Design and Applications involve the standards for building and Rendering Web pages,
              including HTML, CSS, SVG, Ajax, and other technologies for Web Applications ("WebApps"). This section also includes information on how to make
              pages accessible to people with disabilities (WCAG), to internationalize them, and make them work on mobile devices. </p>
            <br />
            <p class="justify process_prg"><b>Web Architecture :</b>Web Architecture focuses on the foundation technologies and principles which sustain
              the Web, including URIs and HTTP. </p>
            <br />
            <p class="justify process_prg"> <b>Semantic Web :</b> In addition to the classic "Web of documents" W3C is helping to build a technology stack to support a 
              "Web of data", the sort of data you find in databases. The ultimate goal of the Web of data is to enable computers to do more useful work
              and to develop systems that can support trusted interactions over the network. The term "Semantic Web" refers to W3C's vision of the Web of
              linked data. Semantic Web technologies enable people to create data stores on the Web, build vocabularies, and write rules for handling data.
              Linked data are empowered by technologies such as RDF, SPARQL, OWL, and SKOS. </p>
            <br />
            <p class="justify process_prg"> <b>XML Technology :</b> XML Technologies including XML, XML Name spaces, XML Schema, XSLT, Efficient XML Interchange (EXI), and other related standards.</p>
            <br />
            <p class="justify process_prg"> <b>Web of Services :</b> Web of Services refers to message-based design frequently found on the Web and in enterprise software.
              The Web of Services is based on technologies such as HTTP, XML, SOAP, WSDL, SPARQL, and others. </p>
            <br />
            <p class="justify process_prg"> <b>Web of Devices :</b>W3C is focusing on technologies to enable Web access anywhere, any time, using any device. This includes
              Web access from mobile phones and other mobile devices as well as use of Web technology in consumer electronics, printers, interactive television, 
              and even automobiles. </p>
