            <p class="justify process_prg">W3C standards define an <b>Open Web Platform</b> for application development that has the unprecedented potential
              to enable developers to build rich interactive experiences, powered by vast data stores, that are available on any device. Although 
              the boundaries of the platform continue to evolve, industry leaders speak nearly in unison about how HTML5 will be the cornerstone 
              for this platform. But the full strength of the platform relies on many more technologies that W3C and its partners are creating, 
              including CSS, SVG, WOFF, the Semantic Web stack, XML, and a variety of APIs.</p>
            <br />
            <p class="justify process_prg">W3C develops these technical specifications and guidelines through a process designed to maximize consensus about the content of a 
              technical report, to ensure high technical and editorial quality, and to earn endorsement by W3C and the broader community. </p>
