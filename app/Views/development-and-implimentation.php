              sensory or interaction modes. We should be able to access the web from any kind of hardware that can connect to the Internet - stationary or mobile, 
              small or large. W3C facilitates this listening and blending via international web standards. These standards ensure that all the crazy brilliance 
              continues to improve a web that is open to us all. </p>
